#!/usr/bin/with-contenv bashio
# ==============================================================================
# Traefik Dynamic Configuration Generator
# Shared helper for all Home Assistant addons in this repository.
#
# Reads traefik_* options from addon config, resolves the host IP and
# port mapping, then writes a Traefik File Provider YAML to
# /share/traefik/dynamic/<slug>.yml
#
# This file is the CANONICAL copy. Each addon has an identical copy at
# rootfs/usr/local/bin/generate-traefik-config.sh
# ==============================================================================

# Addon slug (from config.yaml)
ADDON_SLUG=$(bashio::addon.slug)
TRAEFIK_DIR="/share/traefik/dynamic"
CONFIG_FILE="${TRAEFIK_DIR}/${ADDON_SLUG}.yml"

# Read Traefik options
TRAEFIK_DOMAIN=$(bashio::config 'traefik_domain')
TRAEFIK_ENTRYPOINTS=$(bashio::config 'traefik_entrypoints')
TRAEFIK_CERTRESOLVER=$(bashio::config 'traefik_certresolver')

if [ -z "${TRAEFIK_DOMAIN}" ]; then
    bashio::log.error "traefik_domain is required when traefik_enable is true"
    return 1 2>/dev/null || exit 1
fi

# Resolve host IP via Supervisor network info
# Filters out Docker bridge (172.x) and loopback (127.x) addresses
resolve_host_ip() {
    local net_info ip_addr
    net_info=$(curl -s -H "Authorization: Bearer ${SUPERVISOR_TOKEN}" \
        http://supervisor/network/info 2>/dev/null) || true

    # Try to get the primary interface IP
    ip_addr=$(echo "${net_info}" | jq -r '
        .data.interfaces[]
        | select(.primary == true)
        | .ipv4.address[0]
        // empty' 2>/dev/null | cut -d/ -f1)

    # Fallback: first non-docker, non-loopback interface
    if [ -z "${ip_addr}" ]; then
        ip_addr=$(echo "${net_info}" | jq -r '
            .data.interfaces[]
            | select(.interface | test("^(docker|br-|veth|lo)") | not)
            | .ipv4.address[0]
            // empty' 2>/dev/null | head -1 | cut -d/ -f1)
    fi

    # Last resort: hostname -i
    if [ -z "${ip_addr}" ]; then
        ip_addr=$(hostname -i 2>/dev/null | awk '{print $1}')
    fi

    echo "${ip_addr}"
}

HOST_IP=$(resolve_host_ip)
if [ -z "${HOST_IP}" ] || echo "${HOST_IP}" | grep -qE '^(172\.|127\.)'; then
    bashio::log.warning "Could not resolve a LAN IP (got: ${HOST_IP}). Traefik config may not route correctly."
fi

bashio::log.info "Generating Traefik config: ${TRAEFIK_DOMAIN} -> ${HOST_IP}"

# Create the output directory
mkdir -p "${TRAEFIK_DIR}"

# Router name: sanitize slug for Traefik (replace _ with -)
ROUTER_NAME=$(echo "${ADDON_SLUG}" | tr '_' '-')

# Determine service port and scheme
# Addons can set these via environment before calling this script:
#   TRAEFIK_PORT=9443 TRAEFIK_SCHEME=https generate-traefik-config.sh
# Defaults: read from config.yaml ingress_port, scheme=http
SERVICE_PORT="${TRAEFIK_PORT:-$(bashio::addon.ingress_port 2>/dev/null || echo "")}"
SERVICE_SCHEME="${TRAEFIK_SCHEME:-http}"

if [ -z "${SERVICE_PORT}" ]; then
    bashio::log.error "Cannot determine service port for Traefik config"
    return 1 2>/dev/null || exit 1
fi

# Build the YAML config
cat > "${CONFIG_FILE}" << TRAEFIKEOF
# Auto-generated by ${ADDON_SLUG} HA addon — do not edit manually
# Regenerated on every addon start. Delete to remove routing.
http:
  routers:
    ${ROUTER_NAME}:
      rule: "Host(\`${TRAEFIK_DOMAIN}\`)"
      entryPoints:
        - ${TRAEFIK_ENTRYPOINTS}
      service: ${ROUTER_NAME}
      tls:
        certResolver: ${TRAEFIK_CERTRESOLVER}
  services:
    ${ROUTER_NAME}:
      loadBalancer:
        servers:
          - url: "${SERVICE_SCHEME}://${HOST_IP}:${SERVICE_PORT}"
TRAEFIKEOF

# For HTTPS backends, add insecureSkipVerify (self-signed certs)
if [ "${SERVICE_SCHEME}" = "https" ]; then
    cat >> "${CONFIG_FILE}" << TLSEOF
        serversTransport: ${ROUTER_NAME}-transport
  serversTransports:
    ${ROUTER_NAME}-transport:
      insecureSkipVerify: true
TLSEOF
fi

# Handle extra routes (multi-port addons like MuninnDB)
# Set TRAEFIK_EXTRA_ROUTES as a space-separated list of "name:port:domain" triples
# Example: TRAEFIK_EXTRA_ROUTES="api:8475:api.example.com grpc:8477:grpc.example.com"
if [ -n "${TRAEFIK_EXTRA_ROUTES}" ]; then
    for route in ${TRAEFIK_EXTRA_ROUTES}; do
        local_name=$(echo "${route}" | cut -d: -f1)
        local_port=$(echo "${route}" | cut -d: -f2)
        local_domain=$(echo "${route}" | cut -d: -f3)
        route_id="${ROUTER_NAME}-${local_name}"

        cat >> "${CONFIG_FILE}" << EXTRAEOF
    ${route_id}:
      rule: "Host(\`${local_domain}\`)"
      entryPoints:
        - ${TRAEFIK_ENTRYPOINTS}
      service: ${route_id}
      tls:
        certResolver: ${TRAEFIK_CERTRESOLVER}
EXTRAEOF

        # Append under services (need to add to the existing services block)
        # Since YAML is order-independent for keys, we just append
        cat >> "${CONFIG_FILE}" << SVCEOF
    ${route_id}:
      loadBalancer:
        servers:
          - url: "http://${HOST_IP}:${local_port}"
SVCEOF
    done
fi

bashio::log.info "Traefik config written: ${CONFIG_FILE}"
bashio::log.info "  Router: ${ROUTER_NAME} -> ${SERVICE_SCHEME}://${HOST_IP}:${SERVICE_PORT}"
